package br.com.entidade;

public class Reserva {
	
	private int id;
	private String desc;
	private String dtcada;
	private String dtreser;
	private String hora;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}
	public String getDtcada() {
		return dtcada;
	}
	public void setDtcada(String dtcada) {
		this.dtcada = dtcada;
	}
	public String getDtreser() {
		return dtreser;
	}
	public void setDtreser(String dtreser) {
		this.dtreser = dtreser;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	
	

}
